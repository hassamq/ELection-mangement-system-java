    private void addbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addbtnMouseClicked
        // TODO add your handling code here:
        int age;
        /*String cnic,first,second,father,gender,address;
        cnic=jcnic.getText();
        first=jfname.getText();
        second=jlname.getText();
        father=jfathern.getText();
        gender=jgender.getSelectedItem().toString();
        address=jaddress.getText();*/
         if(capital.isSelected()){
             province="-";
             city="Islamabad";
         }
         else{
             province=jprovince.getSelectedItem().toString();
             city=jcity.getSelectedItem().toString();
         }
         
        
        
        Integer s = new Integer(jage.getText());
        if(s>=18){
            age=s;
            voters v = new voters(jcnic.getText(),jfname.getText() ,jlname.getText() , jfathern.getText() , jgender.getSelectedItem().toString() , province,  city, jaddress.getText(), age);
            
                                                                ///*ADDING DATA*///
            try {
                Connection con =  DriverManager.getConnection("jdbc:derby://localhost:1527/projectdb","hassam","admin");
                String sql = "insert into voters values(?,?,?,?,?,?,?,?,?)";
                PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, v.getCnic());
            ps.setString(2, v.getFirstname());
            ps.setString(3, v.getSecondname());
            ps.setString(4, v.getFathername());
            ps.setInt(5, v.getAge());
            ps.setString(6, v.getProvince());
             ps.setString(7, v.getCity());
              ps.setString(8, v.getAddress());
               ps.setString(9, v.getGender());
                ps.execute();

            JOptionPane.showMessageDialog(null, "Data Inserted Sucessfully");
            
                
            } catch (SQLException ex) {
                 

            JOptionPane.showMessageDialog(null, "Data already exists");
                //Logger.getLogger(addvoter.class.getName()).log(Level.SEVERE, null, ex);
            }
            
           
            
            
            
        }
        else{
            JOptionPane.showMessageDialog(null, "Age must greater than 18");
        }
    }//GEN-LAST:event_addbtnMouseClicked

